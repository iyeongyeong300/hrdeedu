<li><span>5. '기본서비스'란 기본회원이 로그인하여 이용할 수 있는 서비스를 말합니다.</span></li>

<li><span>6. '고급서비스'란 정회원이 로그인하여 이용할 수 있는 서비스를 말합니다.</span></li>
