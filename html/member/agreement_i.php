<li><span>3. 허위 광고 게시, 허위 사실 유포 등 고의적으로 타인을 기만했을 경우</span></li>

<li><span>4. 기타 불법적인 방법으로 회원자격을 취득한 행위가 밝혀진 경우</span></li>

<li><span>5. 타 회원의 개인 정보를 수집, 저장하는 경우</span></li>

