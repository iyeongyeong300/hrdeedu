<li><span>3. 무료회원은 HRDE평생교육원.com 사이트 왼쪽에 있는 유료전환메뉴를 이용하여 정회원이 될 수 있습니다. 이 때 일정액의 이용료를 지불하게 됩니다.</span></li>

