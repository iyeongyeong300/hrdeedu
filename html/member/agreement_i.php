<li><span>3. 욕설, 음담패설 등 타인에게 불쾌감을 주는 경우</span></li>

<li><span>4. 회원 본인의 개인정보가 아닌 타인의 개인정보를 공개하는 경우 단, 정보 공개의 대상이 누구나</span></li>

