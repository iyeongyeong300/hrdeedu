<li><span>3. 고급서비스는 정회원에게 제공되는 서비스로서, 기본서비스에 '모든 파워강좌'의 읽기 권한, '소스창고'의 다운로드 권한 및 한줄 글쓰기가 가능합니다.</span></li>
